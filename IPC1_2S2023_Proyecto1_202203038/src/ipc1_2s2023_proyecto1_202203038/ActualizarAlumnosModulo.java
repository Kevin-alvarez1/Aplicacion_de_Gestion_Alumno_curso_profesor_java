    private void compararYMostrarDatos() {
        boolean coincidePosicion5 = false;

        for (Vector<String> fila : vectorParaCrearBotonesAlumno) {
            if (fila.size() >= 3) {
                String correoUsuario = fila.get(0);

                for (Vector<String> datos : vectorAlumnos) {
                    if (datos.size() >= 4 && datos.get(3).equals(correoUsuario)) {
                        coincidePosicion5 = true;
                        NombreActualizadoAlumno.setText(datos.get(1)); // Posición 1: Nombre
                        ApellidoActualizadoAlumno.setText(datos.get(2)); // Posición 2: Apellido
                        CorreoActualizadoAlumno.setText(correoUsuario); // Correo del usuario en vectorParaCrearBotones
                        GeneroActualizadoAlumnocomboBox.setSelectedItem(datos.get(4)); // Posición 4: Género
                        ContraseñaActualizadoAlumno.setText(datos.get(5)); // Posición 5: Contraseña
                        break;
                    }
                }
                if (coincidePosicion5) {
                    break;
                }
            }
        }

        if (!coincidePosicion5) {
            JOptionPane.showMessageDialog(null, "No se encontraron datos para el usuario ingresado en modo 'Online'", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
private void compararYMostrarDatosOnline() {
    boolean coincidePosicion2 = false;

    for (Vector<String> fila : vectorParaCrearBotonesAlumno) {
        if (fila.size() > 2 && "Online".equals(fila.get(2))) {
            coincidePosicion2 = true;
            break;
        }
    }

    if (coincidePosicion2) {
        compararYMostrarDatos();
    } else {
        JOptionPane.showMessageDialog(null, "No se encontraron usuarios en modo 'Online'", "Error", JOptionPane.ERROR_MESSAGE);
    }
}

  

private void actualizarVectorDeDatos() {

    for (Vector<String> fila : vectorAlumnos) {
            fila.set(1, NombreActualizadoAlumno.getText()); // Posición 1: Nombre
            fila.set(2, ApellidoActualizadoAlumno.getText()); // Posición 2: Apellido
            fila.set(4, GeneroActualizadoAlumnocomboBox.getSelectedItem().toString()); // Posición 3: Género
            fila.set(5, ContraseñaActualizadoAlumno.getText()); // Posición 4: Contraseña
            // No es necesario actualizar la posición 5, ya que representa el correo (usuarioIngresado)

            JOptionPane.showMessageDialog(null, "Datos actualizados correctamente");
            break; // Salir del bucle una vez que se actualice la fila
        
    }
}
