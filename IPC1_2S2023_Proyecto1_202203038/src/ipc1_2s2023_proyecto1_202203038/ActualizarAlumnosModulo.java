 private void seleccionarImagen() {
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos PNG", "png");
        fileChooser.setFileFilter(filter);

        int resultado = fileChooser.showOpenDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivoSeleccionado = fileChooser.getSelectedFile();

            try {
                // Cargar la imagen desde el archivo seleccionado
                Image imagen = ImageIO.read(archivoSeleccionado);
                
                // Ajustar el tamaño de la imagen al tamaño del JLabel
                imagen = imagen.getScaledInstance(FotoLabel.getWidth(), FotoLabel.getHeight(), Image.SCALE_SMOOTH);

                // Mostrar la imagen en el JLabel
                FotoLabel.setIcon(new ImageIcon(imagen));
            } catch (IOException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al cargar la imagen", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
